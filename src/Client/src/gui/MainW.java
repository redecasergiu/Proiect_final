    private void jNewCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jNewCActionPerformed
        try {
            String convName = jCName.getText();
            if (convName != null && convName.length() != 0) {
                c.createConversation(convName);
            }
        } catch (IOException ex) {
            Logger.getLogger(MainW.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_jNewCActionPerformed

    private void jOpenCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jOpenCActionPerformed
        int[] selected = jCs.getSelectedIndices();
        for (int i : selected) {
            Conversation c = this.conversations.get(i);
            ConversationW w = new ConversationW(this, c);
            int convId = c.getConversationId();
            cws.put(convId, w);
            addChild(w);
            w.setVisible(true);
            w.setTitle("YChat - Conversation: " + c.getName());
            w.updateParticipants(new ArrayList<String>());
            Window.c.updateParticipants(convId, new ArrayList<>());
            Window.c.getMessages(convId);
        }
    }//GEN-LAST:event_jOpenCActionPerformed

