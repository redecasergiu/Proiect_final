    public void updateConversations(List<Conversation> cl) {
        this.conversations = cl;

        String[] listData = cl.stream().map((x) -> x.getName()).toArray(String[]::new); //list of the names of the conversations
        //Map conversations = cl.stream().collect(Collectors.toMap(x->x.getConversationId(), Conversation::getName));        
        DefaultListModel<String> model = new DefaultListModel<>();
        for (String i : listData) {
            model.addElement(i);
        }
        jCs.setModel(model);
    }
    
    
    public void updateMessages(int conversationId, String newMessage) {
        if (cws.containsKey(conversationId))
            cws.get(conversationId).updateMessages(newMessage);
    }
    
    
    /**
     * Send message to the client root to send it to the server.
     * @param conversationId
     * @param msg
     * @throws IOException 
     */
    public void sendMessage(int conversationId, Message msg) throws IOException{
        this.c.sendMessage(conversationId, msg);
    }
    
    
    /**
     * Update the participants from the client conversation windows
     * @param conversationId
     * @param participants
     * @throws IOException 
     */
    public void updateParticipants(int conversationId, List<String> participants) throws IOException{
        if (cws.containsKey(conversationId))
            cws.get(conversationId).updateParticipants(participants);
    }
    
    /**
     * Add participants to the conversation, by sending them to the server.
     * @param conversationId
     * @param participants
     * @throws IOException 
     */
    public void addParticipants(int conversationId, List<String> participants) throws IOException{
        Window.c.updateParticipants(conversationId, participants);
    }
    
    protected void removeChild(int childId){
        this.cws.remove(childId);
    }
    
    /**
     * Update the messages from the conversations.
     * @param convId
     * @param messages 
     */
    public void receiveMessages(int convId, List<Message> messages){
        //write the messages as a string
        String msgs = "";
        for (Message m : messages){
            msgs += m.getOwner()+": "+m.getContent()+"\n";
        }
        if (cws.containsKey(convId))
            cws.get(convId).initConversation(msgs);
    }
    
